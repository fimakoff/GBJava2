package lesson1.HW.method;

public class Threadmill {
    private int length;

    public Threadmill() {
        this.length = length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getLength() {
        return length;
    }

    @Override
    public String toString() {
        return "Length of threadmill: " + length;
    }
}
